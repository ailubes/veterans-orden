export function getArticleBySlug(slug: string): NewsArticle | undefined {
  return newsArticles.find((article) => article.slug === slug);
}

export function getRelatedArticles(currentSlug: string, limit: number = 3): NewsArticle[] {
  return newsArticles.filter((article) => article.slug !== currentSlug).slice(0, limit);
}

export function getFeaturedArticles(limit: number = 3): NewsArticle[] {
  return newsArticles.slice(0, limit);
}
